// C++ program to demonstrate the taking multiple inputs from the user 

#include <iostream> 
using namespace std; 

// Driver Code 
int main() 
{ 
	string name; 
	int age; 

	// Take multiple input using cin 
	cin >> name >> age; 

	// Print output 
	cout << "Name : " << name << endl; 
	cout << "Age : " << age << endl; 

	return 0; 
}
